public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("i is now " + 1);
        }
        for (int countNumbers = 1; countNumbers <= 50; countNumbers++) {
            System.out.println("The number is " + countNumbers);
        }
    }
}
